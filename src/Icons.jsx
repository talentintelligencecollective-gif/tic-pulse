export function ToneIndicator({ value }) {
  if (value === null || value === undefined) return null;
  const color = value > 1 ? "var(--accent)" : value < -1 ? "var(--red)" : "var(--text-muted)";
  const arrow = value > 1 ? "↑" : value < -1 ? "↓" : "→";
  return <span style={{ color, fontSize: "12px", fontWeight: 700, lineHeight: 1 }}>{arrow}</span>;
}

